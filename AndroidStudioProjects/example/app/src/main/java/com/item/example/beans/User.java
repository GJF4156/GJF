package com.item.example.beans;

public class User {

        public int id;
        public String username;
        public String password;
        public String sex;
        public String birthdata;

    public int getId() {
        return id;
    }

    public String getUsername() {
        return username;
    }

    public String getPassword() {
        return password;
    }

    public String getSex() {
        return sex;
    }

    public String getBirthdata() {
        return birthdata;
    }
}
